package specifier;

public class MapParams
{

	public int LargestFeat;
	public float Persistence;
	public int Seed;
	public float Frequency;
	public float Frequencies;
	
	public MapParams(int LF, float P, int S, float F, float FS)
	{
		this.LargestFeat = LF;
		this.Persistence = P;
		this.Seed = S;
		this.Frequency = F;
		this.Frequencies = FS;
	}
}
